#include <unistd.h>

void	ft_putnbr_fd(int nb, int fd)
{
	int		num;

	num = 0;
	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	if (nb / 10 != 0)
		ft_putnbr_fd(nb / 10, fd);
	num = nb % 10;
	write (fd, &"0123456789"[num], 1);
}
